/*- Define Vax data types ---------------------------------------------------*/

typedef struct {                        /*  Fixed-string descriptor:         */
    word  Length;                       /*    Length of string in bytes      */
    byte  Dtype;                        /*    Must be DSC_K_DTYPE_T          */
    byte  Class;                        /*    Must be DSC_K_CLASS_S          */
    char *Value;                        /*    Address of start of string     */
} dsc_descriptor_s;

#define desc                             dsc_descriptor_s
#define String(name,string)              name = {                        \
                                             sizeof (string) - 1,        \
                                             DSC_K_DTYPE_T,              \
                                             DSC_K_CLASS_S,              \
                                             string }

typedef struct {                        /*  Shared memory area descriptor:   */
    long  Size;                         /*    Size of shared memory area     */
    void *Start;                        /*    Start of area, if required     */
} mem_descriptor;

typedef struct {                        /*  Logical name item descriptor     */
    word  Size;                         /*    Size of buffer                 */
    word  Code;                         /*    Item code, LNM_STRING          */
    char *Value;                        /*    Points to start of buffer      */
    long *RetSize;                      /*    Returned value size            */
} lnm_descriptor;

typedef struct {                        /*  IO status block for QIO calls    */
    word Status;                        /*    Status after call (0 = ok)     */
    word Length;                        /*    Number of bytes processed      */
    long Other;                         /*    Various purposes               */
} IOSB;

typedef struct {                        /*  ACMS identifier block            */
    long Id [2];                        /*    consists of quadword           */
} ACMSID;

typedef struct {                        /*  ACMS return status block         */
    long Status;                        /*    Status after call              */
    long Other;                         /*    Unspecified purpose            */
} ACSB;

typedef struct {                        /*  Terminal characteristics block   */
    byte Class;                         /*    Device class                   */
    byte Type;                          /*    Device type                    */
    word Width;                         /*    Page width                     */
    long Char1,                         /*    Basic characteristics          */
         Char2;                         /*    Extended characteristics       */
} TERM_MODE;


